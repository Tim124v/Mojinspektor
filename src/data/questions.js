function dedupeQuestionsAndFixOptions(raw) {
  const seenExact = new Set()
  const seenPrefix = new Set()
  const out = []
  const fillerWrong = [
    'Варіант не відповідає польській державній реальності (перевірте факт)',
    'Інший історичний контекст — не для цієї відповіді',
    'Так може бути в іншій країні Європи, але не тут',
    'Поширена плутанина на картках — неправильно для Польщі',
  ]
  let f = 0
  for (const q of raw) {
    if (!q || !q.question) continue
    const norm = String(q.question).trim().toLowerCase().replace(/\s+/g, ' ')
    if (!norm) continue
    const pref = norm.slice(0, 30)
    if (seenExact.has(norm) || seenPrefix.has(pref)) continue
    seenExact.add(norm)
    seenPrefix.add(pref)
    const opts = Array.isArray(q.options) ? [...q.options] : []
    const used = new Set()
    for (let i = 0; i < opts.length; i++) {
      const k = String(opts[i] ?? '').trim().toLowerCase()
      if (!k) continue
      if (used.has(k)) {
        let repl
        do {
          repl = `${fillerWrong[f % fillerWrong.length]} (${f + 1})`
          f++
        } while (used.has(repl.toLowerCase()))
        opts[i] = repl
        used.add(repl.toLowerCase())
      } else {
        used.add(k)
      }
    }
    out.push({ ...q, options: opts })
  }
  return out
}

